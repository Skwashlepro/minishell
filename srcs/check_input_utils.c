#include "minishell.h"

int	check_space(char c)
{
	return ((c >= 9 && c <= 13) || c == 32);
}

int	inquotes(char c, int i)
{
	if ((c == '"' || c == '\'') && i == 0)
		i = 1;
	else if ((c == '"' || c == '\'') && i == 1)
		i = 0;
	return (i);
}